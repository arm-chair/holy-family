import { readingsById } from "./generatedReadings";
import type { ChoiceOption, ChoiceSet } from "./types";

const slug = (value: string) =>
  value
    .toLowerCase()
    .replace(/['"’]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");

const hymn = (title: string): ChoiceOption => ({
  id: slug(title),
  title,
  kind: "hymn",
  searchTerms: `Catholic funeral hymn ${title}`,
});

const reading = (
  id: string,
  title: string,
  originalLabel?: string,
  note?: string,
): ChoiceOption => {
  const source = readingsById[id];

  return {
    id,
    title,
    kind: "reading",
    citation: source?.citation,
    originalLabel,
    note,
    reading: source,
  };
};

const psalm = (id: string, title: string): ChoiceOption => ({
  id,
  title,
  kind: "psalm",
  citation: readingsById["psalm-23"].citation,
  reading: readingsById["psalm-23"],
  searchTerms: `Catholic funeral responsorial psalm ${title}`,
});

